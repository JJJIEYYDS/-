void solve()
{
    ll a, b, c;
    cin >> n >> a >> b >> c;
    ll ans = 1e10;

    for (int i = 0; i <= n; i++)
    {
        for (int j = 0; j <= n; j++)
        {
            for (int k = 0; k <= n; k++)
            {
                if (i * 6 + j * 8 + k * 12 >= n)
                {
                    ans = min(ans, i * a + b * j + c * k);
                }
            }
        }
    }
    cout << ans << endl;
}

